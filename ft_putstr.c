#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	k;

	k = 0;
	if (!s)
		return (ft_putstr("(null)"));
	while (s[k] != '\0')
	{
		ft_putchar(s[k]);
		k++;
	}
	return (k);
}
